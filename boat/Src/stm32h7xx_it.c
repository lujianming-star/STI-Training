//中断处理函数处理数据，中断回调函数执行程序
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{ if(huart->Instance==USART2)
	{	uint8_t turn_left[]="turn_left \n";
		uint8_t turn_right[]="turn_right \n";
		uint8_t go_straight[]="go_straight \n";
		uint8_t stop[]="stop \n";
		switch(Res)
			{	
			case 0x31: {	straight();
							//不能加delay
						HAL_UART_Transmit(&huart2,go_straight,sizeof(go_straight),10000);//发送字符串数组
						HAL_UART_Receive_IT(&huart2,&Res,1);
						
						break;
					}
			case 0x32: {	left();
							
						HAL_UART_Transmit(&huart2,turn_left,sizeof(turn_left),10000);
						HAL_UART_Receive_IT(&huart2,&Res,1);
						
						break;
					}
			case 0x33: {	right();
							
						HAL_UART_Transmit(&huart2,turn_right,sizeof(turn_right),10000);
						HAL_UART_Receive_IT(&huart2,&Res,1);
						
						break;
					}
			case 0x34: {	pause();
							
						HAL_UART_Transmit(&huart2,stop,sizeof(stop),10000);
						HAL_UART_Receive_IT(&huart2,&Res,1);
						break;
					}
		}
	}

}
